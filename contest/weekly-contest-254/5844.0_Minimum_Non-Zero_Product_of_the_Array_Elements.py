'''
贪心+快速幂
策略：尽可能让大数更大，小数更小。
极端一点，最小数都变成1.

比如，n = 4
2 ** 4 - 1 = 15

1 0001     14 1110
2 0010     13 1101
3 0011     12 1100
4 0100     11 1011
5 0101     10 1010
6 0110     9  1001
7 0111     8  1000
15 1111
这样配对，每一对都是 1 * 14，最有有一个15
'''
class Solution:
    def minNonZeroProduct(self, p: int) -> int:
        MOD = 10 ** 9 + 7
        return (((1 << p) - 1) * pow((1 << p) - 2, (1 << (p - 1)) - 1, MOD)) % MOD
